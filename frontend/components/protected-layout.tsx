'use client'

import { useEffect } from 'react'
import { useRouter } from 'next/navigation'
import { useAuth } from '@/lib/auth-context'
import { Loader2 } from 'lucide-react'

export function ProtectedLayout({ children, requiredRole }: { children: React.ReactNode; requiredRole?: 'ngo' | 'volunteer' }) {
  const { user, loading } = useAuth()
  const router = useRouter()

  useEffect(() => {
    if (!loading && !user) {
      router.push('/login')
    } else if (!loading && user && requiredRole && user.role !== requiredRole) {
      // Wrong role, redirect to correct dashboard
      router.push(user.role === 'ngo' ? '/coordinator/dashboard' : '/volunteer/portal')
    }
  }, [user, loading, router, requiredRole])

  if (loading) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <Loader2 className="h-8 w-8 animate-spin text-primary" />
      </div>
    )
  }

  if (!user) {
    return null
  }

  if (requiredRole && user.role !== requiredRole) {
    return null
  }

  return <>{children}</>
}
